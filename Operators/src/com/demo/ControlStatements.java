package com.demo;

public class ControlStatements {
	
	
	public static void main(String[] args) {
		
		
			if (5>3) {
				
				System.out.println("5 greater");
				
			} else {
				
				System.out.println("5 is less");

			}
		
			//int key = 1;
			
			String key = "one";
			
			switch (key) {
			case "one":
					
				System.out.println("One");
				
				break;
				
			case "two":
				
				System.out.println("Two");
				
				break;
			case "three":
				
				System.out.println("Three");
				
				break;

			default:
				System.out.println("default");
				break;
			}
			
			
			
		
		
		
	}
	

}
