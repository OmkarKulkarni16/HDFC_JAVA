package com.demo;

public class LogicalOperators {

	public static void main(String[] args) {

			int x = 100 , y = 100;
		
			if( x > 500 && y > 50 ) {
				
				
				System.out.println("true");
				
				
			}
			else {
				
				System.out.println("false");
				
			}
			
			
		
				if( x > 500 ||  y > 50 ) {
				
				
				System.out.println("true");
				
				
			}
			else {
				
				System.out.println("false");
				
			}
			
			
			

	}

}
